public class Search {

    public String status,thumb_nail;

    public Search(){

    }

    public Search(String status, String thumb_nail) {
        this.status = status;
        this.thumb_nail = thumb_nail;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getThumb_nail() {
        return thumb_nail;
    }

    public void setThumb_nail(String thumb_nail) {
        this.thumb_nail = thumb_nail;
    }





}
